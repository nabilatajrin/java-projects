/*
 * To print a tab, we type the backslash character before the letter t, like 
 * this: \t. Without the \ character, the letter “t” will be printed. With it, 
 * a tab is printed. \t is known as an escape sequence. 
 */
package interactiveprograms.escapeSequences;

/**
 *
 * @author nabila
 */
public class Tab {
    public static void main(String[] args){
        System.out.println("Hello\tWorld");
        /*Hello     World*/
    }
}
